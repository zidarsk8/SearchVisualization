    private void generateRandomTableButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generateRandomTableButtonActionPerformed
        // TODO add your handling code here:
        try{
            cols = Integer.parseInt(jColsTextField.getText());
            rows = Integer.parseInt(jRowsTextField.getText());
            faktor = Integer.parseInt(jTextField1.getText());
            double[][] tempTab = TabFun.makeRandomTable(rows, cols);
            TabFun.normalizerajCols(tempTab);
            double[][] t = tempTab.clone();
            
            myTable = new MyTable(t);

            timer.startTimer();
            prTree = new PRTree(myTable, zaporedje, stDimenzij);
            prBuildTimeLabel.setText(timer.returnTimer("gradnja PR drevesa : ", 1)+" sekund");

            t = tempTab.clone();
            timer.startTimer();
            kdTree = new KDTree(tempTab, zaporedje, stDimenzij);
            kdBuildTimeLabel.setText(timer.returnTimer("gradnja KD drevesa : ", 1)+" sekund");
        }catch (Exception e){
            System.out.println(e.toString());
        }
    }//GEN-LAST:event_generateRandomTableButtonActionPerformed

