    private String getVektorString(double[] vektor){
        String s = new String("");
        for (int i=0; i<vektor.length; i++){
            s+=("        ".substring(((""+vektor[i]+"         ").substring(0, 5)).length())+(""+vektor[i]+"              ").substring(0, 5));
        }
        return s;
    }
